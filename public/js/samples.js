function docSVG(title, org, lines, stampColor) {
  const body = lines.map((l, i) => {
    const [label, value] = Array.isArray(l) ? l : ['', l];
    const y = 300 + i * 78;
    return label
      ? `<text x="90" y="${y}" font-size="34" fill="#555">${label}</text>
         <text x="330" y="${y}" font-size="36" font-weight="700" fill="#111">${value}</text>`
      : `<text x="90" y="${y}" font-size="33" fill="#222">${value}</text>`;
  }).join('');
  return `<svg xmlns="http://www.w3.org/2000/svg" width="900" height="1200" viewBox="0 0 900 1200">
    <rect width="900" height="1200" fill="#fdfcf8"/>
    <rect x="40" y="40" width="820" height="1120" fill="none" stroke="#bbb" stroke-width="3"/>
    <rect x="40" y="40" width="820" height="150" fill="#f0efe9"/>
    <text x="450" y="135" font-size="52" font-weight="800" text-anchor="middle" fill="#1a1a1a">${title}</text>
    <text x="90" y="245" font-size="30" fill="#666">발송 기관: ${org}</text>
    ${body}
    <circle cx="740" cy="1050" r="70" fill="none" stroke="${stampColor}" stroke-width="5"/>
    <text x="740" y="1065" font-size="34" font-weight="700" text-anchor="middle" fill="${stampColor}">직인</text>
    <text x="450" y="1140" font-size="24" text-anchor="middle" fill="#999">※ 이 서류는 체험용 가상 문서입니다</text>
  </svg>`;
}

